    private void armarComboB() {

        AlumnoData ad = new AlumnoData();
        List<Alumno> alumnos;

        alumnos = ad.listarAlumnos();

        for (int i = 0; i < alumnos.size(); i++) {

            jcAlumnoSeleccionado.addItem(alumnos.get(i));
        }
    }
